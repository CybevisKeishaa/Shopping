/**
 *
 * @author KEISHA
 */
public class OrderDetailServlet extends BaseRequiredCustomerAuthenticationController {
   
    /** 
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code> methods.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    
     

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response, Customer_User customer)
    throws ServletException, IOException {
        int cusID = customer.getCus_id();
        int orderID = Integer.parseInt(request.getParameter("orderId"));
        OrderDBContext db = new OrderDBContext();
        Order o = db.getOrderByOrderID(orderID, cusID);
        ArrayList<Product> p = db.getProductsByOrderAndCustomer(orderID, cusID);
        
        request.setAttribute("products", p);
        request.setAttribute("order", o);
        request.getRequestDispatcher("../../view/order/orderDetail.jsp").forward(request, response);
        
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response, Customer_User customer)
    throws ServletException, IOException {
        
    }

    /** 
     * Returns a short description of the servlet.
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
